package ru.bprn.printhouse.data.entity;

/**
 * Определяет фазы (этапы) расчета стоимости, чтобы гарантировать правильный порядок выполнения формул.
 */
public enum CalculationPhase {
    /**
     * Фаза подготовки.
     * Расчет базовых переменных, раскладка изделий на листе, определение рабочей области.
     */
    PREPARATION,

    /**
     * Фаза расчета брака и приладки.
     * Формулы на этом этапе могут изменять итоговый тираж и листаж.
     */
    WASTE_CALCULATION,

    /**
     * Фаза технического расчета.
     * Расчет времени работы оборудования, времени ручных операций, количества расходных материалов.
     */
    TECHNICAL_CALCULATION,

    /**
     * Фаза экономического расчета.
     * Расчет себестоимости операций, применение наценок, налогов и финальное округление.
     */
    ECONOMIC_CALCULATION
}